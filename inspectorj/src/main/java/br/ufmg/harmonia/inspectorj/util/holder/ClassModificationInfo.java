package br.ufmg.harmonia.inspectorj.util.holder;

public class ClassModificationInfo {
	public String className;
	public int minorLine;
	public int offSet;
	
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public int getMinorLine() {
		return minorLine;
	}
	public void setMinorLine(int minorLine) {
		this.minorLine = minorLine;
	}
	public int getOffSet() {
		return offSet;
	}
	public void setOffSet(int offSet) {
		this.offSet = offSet;
	}
	
	
	

}
